# Пользователь вводит целое число n.
# Необходимо найти сумму ряда: 1 - 1/2 + 1/3 - 1/4 + 1/5 - 1/6 +  ... + 1/n.

n = int(input())
s = 0
for i in range(1, n+1):
    if i % 2 == 0:
        w = -1/ i
        s += w
    else:
        w = 1/i
        s += w
print(s)

